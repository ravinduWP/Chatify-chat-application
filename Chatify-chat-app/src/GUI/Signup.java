    private void RegisterActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException {//GEN-FIRST:event_RegisterActionPerformed
        // TODO add your handling code here:
        User user = new User();

        
        String regex="^(([^<>()\\[\\]\\\\.,;:\\s@\"]+(\\.[^<>()\\[\\]\\\\.,;:\\s@\"]+)*)|(\".+\"))@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}])|(([a-zA-Z\\-0-9]+\\.)+[a-zA-Z]{2,}))$";
        String email = Email_ar.getText().trim();
        String uname = uname_ar.getText().trim();
        String nname = nname_ar.getText().trim();
        String pwd = pw_ar.getText().trim();
        String conpw = conpw_ar.getText().trim();

        if (email.isEmpty() || uname.isEmpty() || nname.isEmpty() || pwd.isEmpty() || conpw.isEmpty()){
            JOptionPane.showMessageDialog(null,"please fill all fields..!");
        } else if (!email.matches(regex)) {
            JOptionPane.showMessageDialog(null,"Invalid Email");
        }else if (!pwd.equals(conpw)) {
            JOptionPane.showMessageDialog(null,"Passwords Do not match..!");
        } else if (user.checkEmail(email)) {
            JOptionPane.showMessageDialog(null,"Email has Already Registered..!");
        } else {
            user.registerUser(email,uname,nname,pwd);
            Email_ar.setText("");
            uname_ar.setText("");
            nname_ar.setText("");
            pw_ar.setText("");
            conpw_ar.setText("");
        }
    }//GEN-LAST:event_RegisterActionPerformed

