     public void loadDataFromDatabase() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/chatify_db", "root", "1234")) {
            String query = "SELECT Email, UserName, Password, Nickname FROM registerUser";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query);

            while (rs.next()) {
                String email = rs.getString("Email");
                String userName = rs.getString("UserName");
                String password = rs.getString("Password");
                String nickname = rs.getString("Nickname");

                model.addRow(new Object[]{email, userName, password, nickname});
            }

            rs.close();
            st.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

 
