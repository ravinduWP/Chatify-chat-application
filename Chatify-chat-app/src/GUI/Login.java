    private void LoginActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException, IOException {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
        User user = new User();
        String Email = email.getText().trim();
        String pw = pwd.getText().trim();
        String regex="^(([^<>()\\[\\]\\\\.,;:\\s@\"]+(\\.[^<>()\\[\\]\\\\.,;:\\s@\"]+)*)|(\".+\"))@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}])|(([a-zA-Z\\-0-9]+\\.)+[a-zA-Z]{2,}))$";

        if (Email.isEmpty()||pw.isEmpty()){
                JOptionPane.showMessageDialog(null,"Fill all the Fields");
        }else if (!Email.matches(regex)) {
            JOptionPane.showMessageDialog(null,"Invalid Email");
        }else{
            if (user.loginUser(Email,pw)){
                if (user.getUsname().matches("Admin")) {
                    new test1().setVisible(true);
                    this.setVisible(false);

                }else {
                   new client_chat().setVisible(true);
                    this.setVisible(false);
                }
            }else {
                JOptionPane.showMessageDialog(null,"Email and Password does not match");
            }

        }

    }//GEN-LAST:event_LoginActionPerformed

